def remove_duplicates(s):
    # Шаг 1: Проверка на пустую строку
    if not s:
        return s
    
    # Шаг 2: Создание списка для хранения результата
    result = [s[0]]
    
    # Шаг 3: Использование цикла для прохода по каждому символу строки, начиная со второго
    for char in s[1:]:
        # Шаг 4: Сравнение текущего символа с последним символом в списке результата
        if char != result[-1]:
            result.append(char)
    
    # Шаг 5: Преобразование списка символов в строку и возврат результата
    return ''.join(result)

# Пример использования функции
input_str = "aabbccaa"
print(remove_duplicates(input_str))  # Вывод: "abca"
