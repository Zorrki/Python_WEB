def gcd(a, b):
    while b != 0:  # Повторение до тех пор, пока одно из чисел не станет нулем
        a, b = b, a % b  # Обновление значений: большее число заменяется остатком от деления
    return a  # НОД

# Запросить у пользователя два целых числа
num1 = int(input("Введите первое число: "))
num2 = int(input("Введите второе число: "))

# Найти НОД
result = gcd(num1, num2)

print(f"Наибольший общий делитель (НОД) чисел {num1} и {num2} равен: {result}")
