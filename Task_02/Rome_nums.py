def int_to_roman(num):
    # Определяем массивы для числовых значений и их римских эквивалентов
    values = [1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1]
    symbols = ["M", "CM", "D", "CD", "C", "XC", "L", "XL", "X", "IX", "V", "IV", "I"]

    roman_numeral = ""
    i = 0
    
    # Цикл while для повторения процесса преобразования
    while num > 0:
        for _ in range(num // values[i]):  # Определяем количество раз, сколько можно вычесть текущее значение
            roman_numeral += symbols[i]  # Добавляем соответствующий римский символ к результату
            num -= values[i]  # Уменьшаем число на текущее значение
        i += 1  # Переход к следующему элементу массивов значений и символов

    return roman_numeral

# Запросить у пользователя целое число
number = int(input("Введите целое число: "))

# Преобразовать число в римское представление
roman_representation = int_to_roman(number)

print(f"Римское представление числа {number}: {roman_representation}")
